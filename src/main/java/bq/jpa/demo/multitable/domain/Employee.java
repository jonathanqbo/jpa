package bq.jpa.demo.multitable.domain;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.PrimaryKeyJoinColumn;
import javax.persistence.SecondaryTable;
import javax.persistence.SecondaryTables;

/**
 * <b>  </b>
 *
 * <p> </p>
 *
 * @author Jonathan Q. Bo (jonathan.q.bo@gmail.com)
 *
 * Created at Feb 16, 2014 12:35:23 PM
 *
 */
@Entity(name="jpa_multitalbe_employee")
@SecondaryTables({
		@SecondaryTable(name="jpa_multitable_address", pkJoinColumns=@PrimaryKeyJoinColumn(name="pk_employee")),
		@SecondaryTable(name="jpa_multitable_phone", pkJoinColumns=@PrimaryKeyJoinColumn(name="pk_employee"))
})
public class Employee {

	@Id
	@GeneratedValue
	@Column(name="pk_employee")
	private int id;
	
	private String name;
	
	@Column(table="jpa_multitable_address")
	private String street;
	
	@Column(table="jpa_multitable_address")
	private String city;
	
	@Column(table="jpa_multitable_address")
	private String state;
	
	@Column(table="jpa_multitable_phone")
	private String cellphone;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getStreet() {
		return street;
	}

	public void setStreet(String street) {
		this.street = street;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getCellphone() {
		return cellphone;
	}

	public void setCellphone(String cellphone) {
		this.cellphone = cellphone;
	}
	
}
